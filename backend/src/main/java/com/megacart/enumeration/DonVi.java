public enum DonVi {
    CAI,    // Cái (cho sản phẩm đơn lẻ)
    DOI,    // Đôi (cho giày, dép, găng tay)
    BO,     // Bộ (cho set sản phẩm)
    CHAI,   // Chai (cho nước hoa, đồ uống)
    HOP,    // Hộp (cho bánh kẹo, mỹ phẩm)
    GOI,    // Gói (cho các sản phẩm đóng gói nhỏ)
    QUYEN,  // Quyển (cho sách, vở)
    THUNG   // Thùng (khi bán số lượng lớn)
}